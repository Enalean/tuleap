namespace Tuleap\Project\Webhook;

class Webhook implements \Tuleap\Webhook\Webhook
{
    /**
     * @var int
     */
    private $id;
    /**
     * @var string
     */
    private $name;
    /**
     * @var string
     */
    private $url;

    public function __construct($id, $name, $url)
    {
        $this->id   = $id;
        $this->name = $name;
        $this->url  = $url;
    }

    /**
     * @return int
     */
    #[\Override]
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @return string
     */
    #[\Override]
    public function getUrl()
    {
        return $this->url;
    }
}
