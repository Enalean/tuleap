declare(strict_types=1);

namespace Tuleap\Http\Server;

use Psr\Http\Message\ResponseInterface;
use Psr\Http\Message\ServerRequestInterface;
use Psr\Http\Server\MiddlewareInterface;
use Psr\Http\Server\RequestHandlerInterface;

final class RequestHandlerAsMiddleware implements MiddlewareInterface
{
    /**
     * @var RequestHandlerInterface
     */
    private $request_handler;

    public function __construct(RequestHandlerInterface $request_handler)
    {
        $this->request_handler = $request_handler;
    }

    public function process(ServerRequestInterface $request, RequestHandlerInterface $handler): ResponseInterface
    {
        return $this->request_handler->handle($request);
    }
}
