document.observe('dom:loaded', function () {
    setIsAtTop();
    Event.observe(window, 'scroll', setIsAtTop);

    function setIsAtTop() {
        if (window.scrollY === 0) {
            document.body.addClassName('is-at-top');
        } else {
            document.body.removeClassName('is-at-top');
        }
    }
});
