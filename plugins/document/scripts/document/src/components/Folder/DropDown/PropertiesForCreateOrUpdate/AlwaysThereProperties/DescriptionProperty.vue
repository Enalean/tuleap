<template>
    <div class="tlp-form-element">
        <label class="tlp-label" for="document-description">{{ $gettext("Description") }}</label>
        <textarea
            class="tlp-textarea"
            id="document-description"
            name="description"
            v-bind:placeholder="`${$gettext('My useful document description')}`"
            v-bind:value="value"
            v-on:input="oninput"
            data-test="document-property-description"
        ></textarea>
    </div>
</template>
<script setup lang="ts">
import emitter from "../../../../../helpers/emitter";

defineProps<{ value: string }>();

function oninput($event: Event): void {
    if ($event.target instanceof HTMLTextAreaElement) {
        emitter.emit("update-description-property", $event.target.value);
    }
}
</script>
