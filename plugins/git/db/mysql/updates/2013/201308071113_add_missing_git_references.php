class b201308071113_add_missing_git_references extends ForgeUpgrade_Bucket
{

    /**
     * Description of the bucket
     *
     * @return String
     */
    public function description()
    {
        return <<<EOT
Add the missing git references in projects
EOT;
    }

    /**
     * Get the API
     *
     * @return void
     */
    public function preUp()
    {
        $this->db = $this->getApi('ForgeUpgrade_Bucket_Db');
    }

    /**
     * Creation of the column
     *
     * @return void
     */
    public function up()
    {
        $sql    = "INSERT INTO reference_group (reference_id, group_id, is_active)
                   SELECT 30, service.group_id, 1
                   FROM service
                   LEFT JOIN reference_group
                     ON(service.group_id = reference_group.group_id AND reference_id = 30)
                   WHERE short_name = 'plugin_git'
                     AND reference_id IS NULL
                     AND service.group_id > 0";

        $result = $this->db->dbh->exec($sql);

        if ($result === false) {
            $error_message = implode(', ', $this->db->dbh->errorInfo());
            throw new ForgeUpgrade_Bucket_Exception_UpgradeNotComplete($error_message);
        }
    }
}
