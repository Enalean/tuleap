class b201208241122_remove_wrong_crossref extends ForgeUpgrade_Bucket {

    /**
     * Description of the bucket
     *
     * @return String
     */
    public function description() {
        return <<<EOT
Remove all wrong cross ref where git is the source
EOT;
    }

    /**
     * Get the API
     *
     * @return void
     */
    public function preUp() {
        $this->db = $this->getApi('ForgeUpgrade_Bucket_Db');
    }

    /**
     * Creation of the table
     *
     * @return void
     */
    public function up() {
        $sql = 'DELETE FROM cross_references
                WHERE source_type = "git_commit"
                  AND source_id LIKE "/%"';
        $res = $this->db->dbh->exec($sql);
        if ($res === false) {
            throw new ForgeUpgrade_Bucket_Exception_UpgradeNotComplete('An error occured while dropping wrong git cross references: '.implode(', ', $this->db->dbh->errorInfo()));
        }
    }
}

